<?php

namespace AppBundle\Entity;

/**
 * MaMenu
 */
class MaMenu
{
    /**
     * @var string
     */
    private $descripcion;

    /**
     * @var boolean
     */
    private $activo;

    /**
     * @var integer
     */
    private $menuid;


    /**
     * Set descripcion
     *
     * @param string $descripcion
     *
     * @return MaMenu
     */
    public function setDescripcion($descripcion)
    {
        $this->descripcion = $descripcion;

        return $this;
    }

    /**
     * Get descripcion
     *
     * @return string
     */
    public function getDescripcion()
    {
        return $this->descripcion;
    }

    /**
     * Set activo
     *
     * @param boolean $activo
     *
     * @return MaMenu
     */
    public function setActivo($activo)
    {
        $this->activo = $activo;

        return $this;
    }

    /**
     * Get activo
     *
     * @return boolean
     */
    public function getActivo()
    {
        return $this->activo;
    }

    /**
     * Get menuid
     *
     * @return integer
     */
    public function getMenuid()
    {
        return $this->menuid;
    }
}

